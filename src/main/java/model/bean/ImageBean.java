package model.bean;

/**
 *
 * @author rafae
 */
public class ImageBean {
    private String url;
    
    private String data;

    public ImageBean(String url, String data) {
        this.url = url;
        this.data = data;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
    
    
}
